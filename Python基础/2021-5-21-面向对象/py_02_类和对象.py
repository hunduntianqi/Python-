"""
类：
1.类是对一群具有相同特征或者行为的事物的一个统称,是抽象的,不能直接使用
  1)特征被称为属性
  2）行为被称为方法
2.类相当于制造飞机时的图纸,是一个模板,是负责创建对象的！！！
"""

"""
对象：
1.对象是由类创建出来的一个具体存在,可以直接使用
2.由类创建出来的对象,拥有类中定义的属性和方法
3.对象相当于由图纸制造的飞机
注意：在程序开发中,应该先有类,再有对象
"""

"""
类和对象的关系：
1.类是模板,对象是根据类创建出来的,应该先有类,再有对象
2.类只有一个,而对象可以有很多个
  不同的对象之间属性可能会各不相同
3.类中定义了是么属性和方法,对象中就有什么属性和方法，不会多,也不会少！！！
"""
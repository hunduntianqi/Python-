"""
面向过程和面向对象-基本概念：
1）面向过程-怎么做？
2）面向对象-谁来做？
"""

"""
面向过程：
1.把完成某一个需求的所有步骤从头到尾逐步实现
2.根据开发需求,将某些功能独立的代码封装成一个又一个函数
3.最后完成的代码,顺序的调用不同的函数
特点：
1.注重步骤与过程,不注重职责分工
2.如果需求复杂,代码会变得很复杂
3.开发复杂项目,没有固定的套路,开发难度很大！！
"""

"""
面向对象（是比函数更大的封装,根据职责在一个对象中封装多个方法）：
1.在完成某一个需求前,首先确定职责--要做的事情（方法）
2.根据职责确定不同的对象,在对象内部封装不同的方法（多个）
3.最后完成的代码,顺序的让不同的对象调用不同的方法
特点：
1.注重对象和职责,不同的对象承担不同的职责
2.适合应对复杂的需求变化,专门应对复杂项目开发,提供固定的套路
3.需要在面向过程的基础上,在学习一些面向对象的语法！！
"""

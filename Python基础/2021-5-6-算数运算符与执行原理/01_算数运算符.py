"""
1.'+':加,10 + 20 =30
2.'-':减,10 - 20 =-10
3.'*':乘,10 * 20 = 200
4.'/':除,10 / 20 = 0.5
5.'//':取整除,返回除法的整数部分(商),9//2 = 4
6.'%':取余数,返回除法的余数部分 9 % 2 = 1
7.'**':幂,又称次方,乘方,2 ** 3 = 8
Python中,'*'运算符可以用于字符串,计算结果为字符串重复指定次数
'.'*5 = '.....'
"""

print(10 + 20 )
print(10 - 20)
print(10 * 20)
print(10 / 20)
print(30 // 20)
print(10 % 3 )
print(10 ** 3)
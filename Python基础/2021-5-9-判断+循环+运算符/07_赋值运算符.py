"""
python中的赋值运算符：
赋值运算符中间不能有空格！！！
“=”：简单的赋值运算符，c = a + b，将a+b的值赋给c
"+=":加法赋值运算符，c += a等效于 c = c + a
"-=":减法赋值运算符,c -= a等效于 c = c - a
"*=":乘法赋值运算符，c *= a等效于 c = c * a
"/=":除法赋值运算符，c /= a等效于 c = c / a
“//=”：取整除赋值运算符,c //= a等效于 c = c // a
"%=":取模（余数）赋值运算符, c %= a等效于 c = c % a
“**=”：幂赋值运算符，c **= a等效于 c = c ** a
"""
a = 0
sum = 0
while a <=100:
    if a % 2 == 1:
        sum += a
    a += 1
    print(a)
